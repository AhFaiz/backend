#OPERASI ARITMATIKA

#User Memasukkan Nilai
a = int(input(" Masukkan Angka Pertama "))
b = int(input(" Masukkan Angka Kedua "))

# Operasi Pertambahan +
Hasil_1 = a + b 
print(a,'+',b,'=',Hasil_1)

#Operasi Pengurangan -
Hasil_2 = a - b
print(a,'-',b,'=',Hasil_2)

#Operasi Perkalian x
Hasil_3 = a * b
print(a,'*',b,'=',Hasil_3)

#Operasi Pembagian /
Hasil_4 = a / b
print(a,'/',b,'=',Hasil_4)

#Operasi Eksponen **
Hasil_5 = a ** b
print(a,'**',b,'=',Hasil_5)

#Operasi Modulus (Sisa Pembagian) %
Hasil_6 = a % b
print(a,'%',b,'=',Hasil_6)

#Operasi Floor Division
Hasil_7 = a // b
print(a,'//',b,'=',Hasil_7)



